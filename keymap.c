bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        switch (keycode) {
            // dynamically generate these.
            case VRSN:
                SEND_STRING (QMK_KEYBOARD "/" QMK_KEYMAP " @ " QMK_VERSION);
                return false;
            case LED_BLU:
                rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
                rgb_matrix_sethsv_noeeprom(HSV_BLUE);
                return false;

            case LED_GRN:
                rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
                rgb_matrix_sethsv_noeeprom(HSV_GREEN);
                return false;

            case LED_RED:
                rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
                rgb_matrix_sethsv_noeeprom(HSV_RED);
                return false;

            case LED_ORNG:
                rgb_matrix_mode_noeeprom(RGB_MATRIX_SOLID_COLOR);
                rgb_matrix_sethsv_noeeprom(HSV_ORANGE);
                return false;

            case LED_PUPL:
                rgb_matrix_mode_noeeprom(1);
                rgb_matrix_sethsv_noeeprom(HSV_PURPLE);
                return false;

            case L_ERB:
                SEND_STRING("<%=");
                return false;

            case R_ERB:
                SEND_STRING("%>");
                return false;

            case CLAP:
                SEND_STRING(":clap:");
                return false;

            case COFFEE:
                SEND_STRING(":coffee:");
                return false;

            case D_ARROW:
                SEND_STRING(":arrow_down:");
                return false;

            case LAUGH:
                SEND_STRING(":joy:");
                return false;

            case L_ARROW:
                SEND_STRING(":arrow_left:");
                return false;

            case MINUS_ONE:
                SEND_STRING(":-1:");
                return false;

            case NEUTRAL:
                SEND_STRING(":neutral:");
                return false;

            case PLUS_ONE:
                SEND_STRING(":+1:");
                return false;

            case R_ARROW:
                SEND_STRING(":arrow_right:");
                return false;

            case SAD:
                SEND_STRING(":disappointed:");
                return false;

            case SMILE:
                SEND_STRING(":smile:");
                return false;

            case TROLL:
                SEND_STRING(":trollface:");
                return false;

            case U_ARROW:
                SEND_STRING(":arrow_up:");
                return false;

            case WINK:
                SEND_STRING(":wink:");
                return false;

                // MS Teams Emojis
            case TM_100:
                SEND_STRING("(hundredpoints)");
                return false;

            case TM_BOW:
                SEND_STRING("(manbowing)");
                return false;

            case TM_CAKE:
                SEND_STRING("(cake)");
                return false;

            case TM_CLAP:
                SEND_STRING("(clap)");
                return false;

            case TM_CMARK:
                SEND_STRING("(checkmarkbutton)");
                return false;

            case TM_COFE:
                SEND_STRING("(coffee)");
                return false;

            case TM_CSFG:
                SEND_STRING("(crossedfingers)");
                return false;

            case TM_EXPH:
                SEND_STRING("(explodinghead)");
                return false;

            case TM_EYES:
                SEND_STRING("(eyes)");
                return false;

            case TM_HEART:
                SEND_STRING("(heart)");
                return false;

            case TM_PALM:
                SEND_STRING("(facepalm)");
                return false;

            case TM_OKAY:
                SEND_STRING("(ok)");
                return false;

            case TM_PRAY:
                SEND_STRING("(pray)");
                return false;

            case TM_P_ONE:
                SEND_STRING("(y)");
                return false;

            case TM_SMILE:
                SEND_STRING(":)");
                return false;

            case TM_SURP:
                SEND_STRING(":O");
                return false;

            case TM_TADA:
                SEND_STRING("(partypopper)");
                return false;

            case TM_TEA:
                SEND_STRING("(chai)");
                return false;

            case TM_THINK:
                SEND_STRING("(thinkingface)");
                return false;

            case TM_WAVE:
                SEND_STRING("(wavinghand)");
                return false;
        }
    }

    return true;
};
